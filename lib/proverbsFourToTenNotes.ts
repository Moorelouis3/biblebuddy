import {
  deepenProverbsIntro,
  deepenProverbsSection,
  deepenProverbsTakeaway,
} from "./proverbsDeepStudyRenderer";

type Verse = {
  number: number;
  text: string;
};

type NotesSection = {
  range: string;
  heading: string;
  verses: Verse[];
  body: string[];
};

type ChapterNotes = {
  title: string;
  intro: string[];
  flow: string[];
  sections: NotesSection[];
  takeaway: string[];
};

function renderVerseBlock(verses: Verse[]) {
  return verses.map((verse) => `> **${verse.number}** ${verse.text}`).join("\n>\n");
}

function renderNotes(chapter: number, notes: ChapterNotes) {
  const intro = deepenProverbsIntro(chapter, notes.title, notes.intro);
  const takeaway = deepenProverbsTakeaway(chapter, notes.takeaway);

  return `# ${notes.title}

${intro.join("\n\n")}

## 📍 The Chapter Flow

${notes.flow.map((item) => `- ${item}`).join("\n")}

${notes.sections
  .map(
    (section) => `## 📖 Proverbs ${chapter}:${section.range}

${renderVerseBlock(section.verses)}

## ${section.heading}

${deepenProverbsSection({
  chapter,
  title: notes.title,
  range: section.range,
  heading: section.heading,
  body: section.body,
}).join("\n\n")}`,
  )
  .join("\n\n")}

## 💡 The Big Lesson of Proverbs ${chapter}

${takeaway.join("\n\n")}
`;
}

