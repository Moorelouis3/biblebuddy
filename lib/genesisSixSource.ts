type ParsedGenesisSixSection = GenesisSixPersonalSection & {
  displayReference: string;
};

function parseReferenceParts(reference: string) {
  const normalized = reference.replace(/[–—]/g, '-').trim();
  const match = normalized.match(/^Genesis\s+(\d+):(\d+)(?:-(\d+))?$/i);
  if (!match) {
    throw new Error(`Invalid Genesis 6 reference: ${reference}`);
  }

  const [, chapterText, startText, endText] = match;
  const chapter = Number(chapterText);
  const startVerse = Number(startText);
  const endVerse = Number(endText || startText);

  return {
    chapter,
    startVerse,
    endVerse,
    reference: endVerse === startVerse ? `Genesis ${chapter}:${startVerse}` : `Genesis ${chapter}:${startVerse}-${endVerse}`,
  };
}

function parseGenesisSixRawNotes(rawNotes: string): ParsedGenesisSixSection[] {
  const lines = rawNotes.replace(/\r\n/g, '\n').split('\n');
  const sections: ParsedGenesisSixSection[] = [];
  let currentSection: ParsedGenesisSixSection | null = null;
  let currentPhraseTitle = '';
  let currentPhraseBody: string[] = [];

  const flushPhrase = () => {
    if (!currentSection || !currentPhraseTitle) return;
    const body = currentPhraseBody.join('\n').trim();
    currentSection.phrases.push([currentPhraseTitle, body]);
    currentPhraseTitle = '';
    currentPhraseBody = [];
  };

  const flushSection = () => {
    if (!currentSection) return;
    flushPhrase();
    sections.push(currentSection);
    currentSection = null;
  };

  for (const rawLine of lines) {
    const line = rawLine.trimEnd();
    const trimmed = line.trim();

    const sectionMatch = trimmed.match(/^#\s+\*\*(.+?)\*\*$/);
    if (sectionMatch) {
      flushSection();
      const heading = sectionMatch[1].trim();
      const headingMatch = heading.match(/^(.+?)\s+(Genesis\s+\d+:\d+(?:(?:-|–|—)\d+)?)$/);
      const icon = headingMatch?.[1]?.trim() || '';
      const displayReference = headingMatch?.[2]?.trim() || heading;
      const parsed = parseReferenceParts(displayReference);
      currentSection = {
        chapter: parsed.chapter,
        startVerse: parsed.startVerse,
        endVerse: parsed.endVerse,
        reference: parsed.reference,
        displayReference,
        title: '',
        icon,
        phrases: [],
      };
      continue;
    }

    if (!currentSection) continue;

    if (!trimmed) {
      if (currentPhraseTitle) currentPhraseBody.push('');
      continue;
    }

    if (trimmed === '---' || trimmed === '⸻' || /^Verse\s+\d+/i.test(trimmed)) {
      flushPhrase();
      continue;
    }

    const boldMatch = trimmed.match(/^\*\*(.+?)\*\*$/);
    if (boldMatch) {
      const heading = boldMatch[1].trim();
      if (!currentSection.title) {
        currentSection.title = heading;
      } else {
        flushPhrase();
        currentPhraseTitle = heading;
      }
      continue;
    }

    if (!currentPhraseTitle) continue;
    currentPhraseBody.push(line);
  }

  flushSection();
  return sections;
}

function buildGenesisSixDeepNotes(sections: ParsedGenesisSixSection[]) {
  const blocks = sections.map((section) => {
    const phrases = section.phrases
      .map(([heading, body]) => `### ${heading}\n\n${body}`)
      .join('\n\n---\n\n');

    return `# ${section.icon} ${section.displayReference}\n\n## ${section.title}\n\n${phrases}`;
  });

  return blocks.join('\n\n');
}

const parsedGenesisSixSections = parseGenesisSixRawNotes(GENESIS_SIX_RAW_NOTES);

export const GENESIS_SIX_PERSONAL_SECTIONS: GenesisSixPersonalSection[] = parsedGenesisSixSections.map(({ displayReference, ...section }) => section);

export const GENESIS_SIX_DEEP_NOTES = buildGenesisSixDeepNotes(parsedGenesisSixSections);
