function normalizeReference(reference: string) {
  return reference.replace(/[\u2013\u2014]/g, "-").trim();
}

function parseReferenceParts(reference: string) {
  const normalized = normalizeReference(reference);
  const match = normalized.match(/^Genesis\s+(\d+):(\d+)-(\d+)$/i);

  if (!match) {
    throw new Error(`Could not parse Genesis 9 reference: ${reference}`);
  }

  return {
    reference: `Genesis ${match[1]}:${match[2]}-${match[3]}`,
    chapter: Number(match[1]),
    startVerse: Number(match[2]),
    endVerse: Number(match[3]),
  };
}

function parseGenesisNineRawNotes(raw: string): ParsedGenesisNineSection[] {
  const lines = raw.replace(/\r\n/g, "\n").split("\n");
  const sections: ParsedGenesisNineSection[] = [];

  let currentSection: ParsedGenesisNineSection | null = null;
  let currentPhraseTitle = "";
  let currentPhraseBody: string[] = [];

  const flushPhrase = () => {
    if (!currentSection || !currentPhraseTitle) return;
    currentSection.phrases.push([currentPhraseTitle, currentPhraseBody.join("\n").trim()]);
    currentPhraseTitle = "";
    currentPhraseBody = [];
  };

  const flushSection = () => {
    if (!currentSection) return;
    flushPhrase();
    sections.push(currentSection);
    currentSection = null;
  };

  for (const rawLine of lines) {
    const line = rawLine.trimEnd();
    const trimmed = line.trim();

    const sectionMatch = trimmed.match(/^#\s+\*\*(.+?)\*\*$/);
    if (sectionMatch) {
      flushSection();
      const heading = sectionMatch[1].trim();
      const headingMatch = heading.match(/^(.+?)\s+(Genesis\s+\d+:\d+(?:-|\u2013|\u2014)\d+)$/);
      const icon = headingMatch?.[1]?.trim() || "";
      const displayReference = headingMatch?.[2]?.trim() || heading;
      const parsed = parseReferenceParts(displayReference);
      currentSection = {
        chapter: parsed.chapter,
        startVerse: parsed.startVerse,
        endVerse: parsed.endVerse,
        reference: parsed.reference,
        displayReference,
        title: "",
        icon,
        phrases: [],
      };
      continue;
    }

    if (!currentSection) continue;

    if (trimmed === "---") {
      flushPhrase();
      continue;
    }

    const boldMatch = trimmed.match(/^\*\*(.+?)\*\*$/);
    if (boldMatch) {
      const boldText = boldMatch[1].trim();
      if (!currentSection.title) {
        currentSection.title = boldText;
      } else {
        flushPhrase();
        currentPhraseTitle = boldText;
      }
      continue;
    }

    if (!currentPhraseTitle) continue;
    currentPhraseBody.push(line);
  }

  flushSection();
  return sections;
}

const parsedGenesisNineSections = parseGenesisNineRawNotes(GENESIS_NINE_RAW_NOTES);

export const GENESIS_NINE_PERSONAL_SECTIONS: GenesisNinePersonalSection[] = parsedGenesisNineSections.map(({ displayReference, ...section }) => section);
