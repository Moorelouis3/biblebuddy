type ParsedGenesisFiveSection = GenesisFivePersonalSection & {
  displayReference: string;
};

function normalizeReference(reference: string) {
  return reference.replace(/[вҖ“вҖ”]/g, '-').trim();
}

function parseReferenceParts(reference: string) {
  const normalized = normalizeReference(reference);
  const match = normalized.match(/^Genesis\s+(\d+):(\d+)-(\d+)$/i);
  if (!match) {
    throw new Error(`Could not parse Genesis 5 reference: ${reference}`);
  }

  return {
    reference: `Genesis ${match[1]}:${match[2]}-${match[3]}`,
    chapter: Number(match[1]),
    startVerse: Number(match[2]),
    endVerse: Number(match[3]),
  };
}

function parseGenesisFiveRawNotes(raw: string): ParsedGenesisFiveSection[] {
  const lines = raw.replace(/\r\n/g, '\n').split('\n');
  const sections: ParsedGenesisFiveSection[] = [];

  let currentSection: ParsedGenesisFiveSection | null = null;
  let currentPhraseTitle = '';
  let currentPhraseBody: string[] = [];

  const flushPhrase = () => {
    if (!currentSection || !currentPhraseTitle) return;
    currentSection.phrases.push([currentPhraseTitle, currentPhraseBody.join('\n').trim()]);
    currentPhraseTitle = '';
    currentPhraseBody = [];
  };

  const flushSection = () => {
    if (!currentSection) return;
    flushPhrase();
    sections.push(currentSection);
    currentSection = null;
  };

  for (const rawLine of lines) {
    const line = rawLine.trimEnd();
    const trimmed = line.trim();

    const sectionMatch = trimmed.match(/^#\s+(.+)$/);
    if (sectionMatch && !trimmed.startsWith('##') && !trimmed.startsWith('###')) {
      flushSection();
      const heading = sectionMatch[1].trim();
      const headingMatch = heading.match(/^(.+?)\s+(Genesis\s+\d+:\d+(?:-|вҖ“|вҖ”)\d+)$/);
      const icon = headingMatch?.[1]?.trim() || '';
      const displayReference = headingMatch?.[2]?.trim() || heading;
      const parsed = parseReferenceParts(displayReference);
      currentSection = {
        chapter: parsed.chapter,
        startVerse: parsed.startVerse,
        endVerse: parsed.endVerse,
        reference: parsed.reference,
        displayReference,
        title: '',
        icon,
        phrases: [],
      };
      continue;
    }

    if (!currentSection) continue;

    if (trimmed === '---' || trimmed === 'вё»') {
      flushPhrase();
      continue;
    }

    const titleMatch = trimmed.match(/^##\s+(.+)$/);
    if (titleMatch) {
      currentSection.title = titleMatch[1].trim();
      continue;
    }

    const phraseMatch = trimmed.match(/^###\s+(.+)$/);
    if (phraseMatch) {
      flushPhrase();
      currentPhraseTitle = phraseMatch[1].trim();
      continue;
    }

    if (!currentPhraseTitle) continue;
    currentPhraseBody.push(line);
  }

  flushSection();
  return sections;
}

function buildGenesisFiveDeepNotes(sections: ParsedGenesisFiveSection[]) {
  const blocks = sections.map((section) => {
    const phrases = section.phrases
      .map(([heading, body]) => `### ${heading}\n\n${body}`)
      .join('\n\n---\n\n');

    return `# ${section.icon} ${section.displayReference}\n\n## ${section.title}\n\n${phrases}`;
  });

  return blocks.join('\n\n');
}

