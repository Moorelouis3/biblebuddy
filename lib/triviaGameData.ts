function formatAnswer(answer: string) {
  return answer
    .split(/[\s-]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1).toLowerCase())
    .join(" ");
}

function buildOptions(correctAnswer: string, answers: string[]) {
  const distractors: string[] = [];

  for (const candidate of answers) {
    if (candidate === correctAnswer || distractors.includes(candidate)) {
      continue;
    }

    distractors.push(candidate);
    if (distractors.length === 3) {
      break;
    }
  }

  while (distractors.length < 3) {
    distractors.push(`Choice ${distractors.length + 2}`);
  }

  const optionTexts = [correctAnswer, ...distractors].sort((left, right) => left.localeCompare(right));
  return optionTexts.map((text, index) => ({
    label: OPTION_LABELS[index],
    text,
  }));
}

function createGeneratedQuestionId(bookKey: string, chapterNumber: number, questionNumber: number) {
  return `${bookKey}-chapter-${chapterNumber}-question-${questionNumber}`;
}

function buildGeneratedTriviaBook(bookKey: string): TriviaBookPack | null {
  const config = getChapterBasedTriviaBookByKey(bookKey);
  const scrambledBook = getScrambledBook(bookKey);

  if (!config || !scrambledBook) {
    return null;
  }

  return {
    key: config.key,
    routeSlug: config.routeSlug,
    name: config.title,
    chapters: scrambledBook.chapters.map((chapter) => {
      const formattedAnswers = chapter.questions.map((question) => formatAnswer(question.answer));

      return {
        chapter: chapter.chapter,
        title: `Chapter ${chapter.chapter}`,
        description: chapter.description,
        progressKey: getTriviaProgressBookKey(bookKey, chapter.chapter),
        questions: chapter.questions.slice(0, 5).map((question, index) => {
          const correctText = formatAnswer(question.answer);
          const options = buildOptions(correctText, formattedAnswers);

          return {
            id: createGeneratedQuestionId(bookKey, chapter.chapter, index + 1),
            question: `Which Bible word best matches this clue from ${config.title} ${chapter.chapter}?`,
            options,
            correctAnswer: options.find((option) => option.text === correctText)?.label ?? "A",
            verse: question.reference,
            explanation: question.clue,
          };
        }),
      };
    }),
  };
}

const TRIVIA_BOOKS = new Map(
  CHAPTER_BASED_TRIVIA_BOOK_CONFIG.map((book) => [book.key, MANUAL_TRIVIA_BOOKS[book.key] ?? buildGeneratedTriviaBook(book.key)]),
);

export function getTriviaBook(bookKey: string) {
  return TRIVIA_BOOKS.get(bookKey) ?? null;
}

export function getTriviaChapter(bookKey: string, chapterNumber: number) {
  return getTriviaBook(bookKey)?.chapters.find((chapter) => chapter.chapter === chapterNumber) ?? null;
}
